import { useState, useEffect } from "react";
import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import { supabase } from "@/integrations/supabase/client";
import { Session } from "@supabase/supabase-js";
import Index from "./pages/Index";
import BookingPage from "./pages/Booking";
import NotFound from "./pages/NotFound";
import AdminDashboardPage from "./pages/AdminDashboard";
import AuthPage from "@/components/auth/AuthPage";
import ProfilePage from "./pages/Profile";
import ViewForm from "./pages/ViewForm";
import HealthChat from "./pages/HealthChat";

const queryClient = new QueryClient();

const App = () => {
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    supabase.auth.getSession().then(({ data: { session } }) => {
      setSession(session);
      setLoading(false);
    });

    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((_event, session) => {
      setSession(session);
    });

    return () => subscription.unsubscribe();
  }, []);

  if (loading) {
    return (
      <div className="flex justify-center items-center h-screen">
        <div>Loading...</div>
      </div>
    );
  }

  return (
    <QueryClientProvider client={queryClient}>
      <TooltipProvider>
        <Toaster />
        <Sonner />
        <BrowserRouter>
          <Routes>
            <Route path="/" element={<Index />} />
            <Route path="/booking" element={session ? <BookingPage user={session.user} session={session} /> : <AuthPage />} />
            <Route path="/admin-dashboard" element={<AdminDashboardPage />} />
            <Route path="/profile" element={session ? <ProfilePage user={session.user} /> : <AuthPage />} />
            <Route path="/view-form" element={<ViewForm />} />
            <Route path="/health-chat" element={session ? <HealthChat user={session.user} /> : <AuthPage />} />
            {/* ADD ALL CUSTOM ROUTES ABOVE THE CATCH-ALL "*" ROUTE */}
            <Route path="*" element={<NotFound />} />
          </Routes>
        </BrowserRouter>
      </TooltipProvider>
    </QueryClientProvider>
  );
};

export default App;
